"use client";

import { useEffect, useState } from "react";
import { useParams } from "next/navigation";
import { motion } from "framer-motion";
import jsPDF from "jspdf";
import GlassCard from "@/components/ui/GlassCard";
import Button from "@/components/ui/Button";
import Loader from "@/components/ui/Loader";
import Badge from "@/components/ui/Badge";
import { Result } from "@/lib/types";
import { getGrade, formatDate } from "@/lib/utils";
import { APP_NAME } from "@/lib/constants";
import { FiDownload } from "react-icons/fi";

export default function ResultDetailPage() {
  const params = useParams();
  const [result, setResult] = useState<Result | null>(null);
  const [loading, setLoading] = useState(true);

  useEffect(() => {
    fetch(`/api/results/${params.id}`)
      .then((r) => r.json())
      .then((data) => setResult(data.data))
      .finally(() => setLoading(false));
  }, [params.id]);

  const downloadPDF = () => {
    if (!result) return;
    const doc = new jsPDF();
    const grade = getGrade(result.percentage);

    doc.setFontSize(20);
    doc.setTextColor(0, 200, 255);
    doc.text(APP_NAME, 105, 25, { align: "center" });

    doc.setFontSize(10);
    doc.setTextColor(100, 100, 100);
    doc.text("Academic Result Report", 105, 35, { align: "center" });

    doc.setDrawColor(0, 200, 255);
    doc.setLineWidth(0.5);
    doc.line(20, 42, 190, 42);

    doc.setFontSize(12);
    doc.setTextColor(0, 0, 0);

    let y = 55;
    const addLine = (label: string, value: string) => {
      doc.setFont("helvetica", "bold");
      doc.text(`${label}:`, 25, y);
      doc.setFont("helvetica", "normal");
      doc.text(value, 80, y);
      y += 10;
    };

    addLine("Student Name", result.studentName);
    addLine("Father Name", result.fatherName);
    addLine("Class", result.class);
    addLine("Exam", result.examTitle);
    addLine("Total Marks", String(result.totalMarks));
    addLine("Obtained Marks", String(result.obtainedMarks));
    addLine("Percentage", `${result.percentage}%`);
    addLine("Grade", grade);
    addLine("Date", formatDate(result.createdAt));

    doc.setDrawColor(0, 200, 255);
    doc.line(20, y + 5, 190, y + 5);

    doc.setFontSize(8);
    doc.setTextColor(150, 150, 150);
    doc.text(`Generated by ${APP_NAME} | By M. Azam`, 105, y + 15, { align: "center" });

    doc.save(`result-${result.studentName}-${result.examTitle}.pdf`);
  };

  if (loading) return <div className="min-h-[60vh] flex items-center justify-center"><Loader text="Loading Result..." /></div>;
  if (!result) return <div className="text-center py-20 text-white/30">Result not found.</div>;

  const grade = getGrade(result.percentage);

  return (
    <div className="max-w-2xl mx-auto px-4 sm:px-6 lg:px-8 py-8">
      <motion.div initial={{ opacity: 0, y: 30 }} animate={{ opacity: 1, y: 0 }}>
        {/* Header */}
        <div className="text-center mb-8">
          <h1 className="font-display text-2xl font-bold neon-text mb-1">{APP_NAME}</h1>
          <p className="text-white/40 font-body text-sm">Academic Result Report</p>
          <div className="h-px w-32 mx-auto bg-gradient-to-r from-transparent via-neon-blue/50 to-transparent mt-4" />
        </div>

        {/* Result Card */}
        <GlassCard glow>
          {/* Student Info */}
          <h2 className="font-display text-sm uppercase tracking-wider text-neon-blue mb-4">Student Information</h2>
          <div className="grid grid-cols-2 gap-3 mb-6 text-sm">
            <div><span className="text-white/40">Name:</span> <span className="text-white font-medium">{result.studentName}</span></div>
            <div><span className="text-white/40">Father Name:</span> <span className="text-white font-medium">{result.fatherName}</span></div>
            <div><span className="text-white/40">Class:</span> <span className="text-white font-medium">{result.class}</span></div>
            <div><span className="text-white/40">Date:</span> <span className="text-white font-medium">{formatDate(result.createdAt)}</span></div>
          </div>

          {/* Exam Info */}
          <h2 className="font-display text-sm uppercase tracking-wider text-neon-purple mb-4">Exam Information</h2>
          <div className="grid grid-cols-2 gap-3 mb-6 text-sm">
            <div className="col-span-2"><span className="text-white/40">Exam:</span> <span className="text-white font-medium">{result.examTitle}</span></div>
            <div><span className="text-white/40">Total Marks:</span> <span className="text-white font-medium">{result.totalMarks}</span></div>
            <div><span className="text-white/40">Obtained:</span> <span className="text-white font-medium">{result.obtainedMarks}</span></div>
          </div>

          {/* Score */}
          <div className="text-center py-6 border-t border-white/10">
            <div className="flex items-center justify-center gap-8">
              <div>
                <p className="font-display text-5xl font-black neon-text">{result.percentage}%</p>
                <p className="text-xs text-white/40 font-display uppercase tracking-wider mt-1">Percentage</p>
              </div>
              <div>
                <p className={`font-display text-5xl font-black ${result.percentage >= 50 ? "neon-text-green" : "text-red-400"}`}>
                  {grade}
                </p>
                <p className="text-xs text-white/40 font-display uppercase tracking-wider mt-1">Grade</p>
              </div>
            </div>
            <Badge variant={result.percentage >= 50 ? "green" : "red"} className="mt-4">
              {result.percentage >= 50 ? "PASSED" : "FAILED"}
            </Badge>
          </div>
        </GlassCard>

        {/* Download */}
        <div className="mt-6 text-center">
          <Button variant="primary" size="lg" onClick={downloadPDF} className="inline-flex items-center gap-2">
            <FiDownload /> Download Result
          </Button>
        </div>
      </motion.div>
    </div>
  );
}
